int recur(int s_idx, int t_idx){
    if(t_idx == t.size()) return 1e18;
    if(s_idx == s.size()) return 0;
    int &ans = dp[s_idx][t_idx];
    if(ans != -1) return dp[s_idx][t_idx];
    int poss1 = recur(s_idx + 1, t_idx + (s[s_idx] == t[t_idx]));
    int poss2 = v[s_idx] + recur(s_idx + 1, t_idx);
    ans = min(poss1, poss2);
    return ans;
}
