bool check(vector<int>&v){
    for(int i = 0 ; i < v.size(); i++){
        if(v[i] < x) return false;
    }
    return true;
}
void recur(int i ,int cnt, vector<int>&a){
    if( i == n){
        if(check(a)){
            // cout << mini <<" "<<cnt << endl;
            // tr(i, mini, cnt);
            mini = min(mini, cnt);
        }
        return;
    }
    vector<int>temp(m);
    for(int j = 1; j <= m ;j++){
        temp[j - 1] = a[j - 1] + dp[i][j];
    }
    recur(i + 1,cnt +  dp[i][0], temp);
    recur(i + 1, cnt , a);
    return;
}
